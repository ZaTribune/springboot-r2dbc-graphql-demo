create table ORGANIZATION
(
    ID  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME               VARCHAR2(200),
    CREATED_DATE       TIMESTAMP(6) default current_timestamp not null,
    LAST_MODIFIED_DATE TIMESTAMP(6) default current_timestamp not null
);

create table DEPARTMENT
(
    ID  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME               VARCHAR2(200),
    CREATED_DATE       TIMESTAMP(6) default current_timestamp not null,
    LAST_MODIFIED_DATE TIMESTAMP(6) default current_timestamp not null
);

create table EMPLOYEE
(
    ID  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    VERSION            NUMBER,
    FIRST_NAME         VARCHAR2(100),
    LAST_NAME          VARCHAR2(100),
    POSITION           VARCHAR2(200),
    SALARY             NUMBER(10, 2),
    AGE                NUMBER,
    CREATED_DATE       TIMESTAMP(6) default current_timestamp,
    LAST_MODIFIED_DATE TIMESTAMP(6) default current_timestamp,
    ORGANIZATION       NUMBER
        constraint EMPLOYEE_ORGANIZATION_FK
            references ORGANIZATION,
    DEPARTMENT         NUMBER
        constraint EMPLOYEE_DEPARTMENT_FK
            references DEPARTMENT
);


